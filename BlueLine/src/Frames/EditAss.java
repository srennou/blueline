    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        doc.setU_Add(txtmail.getText());
        doc.setU_Nom(txtfirst.getText());
        doc.setU_Prenom(txtlast.getText());
        doc.setU_Tel(txtphone.getText());
         doc.setU_Role("Assistante");
        doc.setU_Pass("");
        int c = dao.Userupdate(doc);
        if (c == 0) {
            JOptionPane.showMessageDialog(this, "erreur");
        } else {
            JOptionPane.showMessageDialog(this, "good");
            dispose();
            
        }


    }//GEN-LAST:event_jButton1ActionPerformed

