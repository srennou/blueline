    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
doc = dao.getById("" + lb, this);
         
     
        txtfirst.setText(String.valueOf(doc.getSll_Num()));
         txtlast.setText(String.valueOf(doc.getSll_Etage()));
        
        
       cbsp.setSelectedItem(doc.getSllType().getTy_Nom());
              cbdept.setSelectedItem(doc.getDepartement().getDep_Nom());
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        doc.setSll_Num(Integer.parseInt(txtfirst.getText()));
        doc.setSll_Etage(Integer.parseInt(txtlast.getText()));
    
        SllType ssp = (SllType) cbsp.getSelectedItem();
        doc.setSllType(ssp);
        
        Departement dept = (Departement) cbdept.getSelectedItem();
        
        doc.setDepartement(dept);
        int c = dao.Salleupdate(doc);
        if (c == 0) {
            JOptionPane.showMessageDialog(this, "erreur");
        } else {
            JOptionPane.showMessageDialog(this, "good");
            dispose();
            
        }


    }//GEN-LAST:event_jButton1ActionPerformed

